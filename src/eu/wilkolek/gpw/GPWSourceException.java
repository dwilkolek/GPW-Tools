package eu.wilkolek.gpw;

/**
 *
 * @author Slishu
 */
public class GPWSourceException extends Throwable{

    public GPWSourceException() {
    }

    public GPWSourceException(String message) {
        super(message);
    }

    public GPWSourceException(String message, Throwable cause) {
        super(message, cause);
    }

    public GPWSourceException(Throwable cause) {
        super(cause);
    }

    public GPWSourceException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

 
    
}
